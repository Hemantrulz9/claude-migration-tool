def resolve_global_config() -> Path:
    """The Claude Code global config path that actually exists, or the preferred default."""
    return first_existing(CLAUDE_CODE_GLOBAL_CANDIDATES) or CLAUDE_CODE_GLOBAL_CANDIDATES[0]


def resolve_desktop_install() -> Path | None:
    """The Claude Desktop install/app-data path that exists, or None if not found."""
    return first_existing(CLAUDE_DESKTOP_INSTALL_CANDIDATES)


def resolve_cowork_vhdx() -> list[Path]:
    """All Cowork VM .vhdx files found across known locations (located, never copied)."""
    found: list[Path] = []
    # Installer build: AppData\Claude\vm_bundles\**\*.vhdx
    found += glob_files(APPDATA / "Claude" / "vm_bundles", "*.vhdx")
    # Store build: Packages\Claude_*\LocalState\**\*.vhdx (shallow into each package's LocalState)
    for pkg in glob_dirs(LOCALAPPDATA / "Packages", "*Claude*"):
        found += glob_files(pkg / "LocalState", "*.vhdx")
    # de-duplicate, preserve order
    seen: set[str] = set()
    unique: list[Path] = []
    for p in found:
        key = str(p).lower()
        if key not in seen:
            seen.add(key)
            unique.append(p)
    return unique


def resolve_cowork_packages() -> list[Path]:
    """Claude_* package folders under LocalAppData\\Packages (Store build), names only."""
    pkgs: list[Path] = []
    for parent in COWORK_PACKAGE_PARENTS:
        pkgs += glob_dirs(parent, "*Claude*")
    return pkgs


