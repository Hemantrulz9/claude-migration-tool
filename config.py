# --------------------------------------------------------------------------- logging
# The spec mandates migration.log and uses log("LEVEL", msg) across modules but lists no
# logger file. config.py is imported everywhere, so the shared logger lives here.
LOG_FILE = Path.cwd() / "migration.log"
_VALID_LEVELS = {"INFO", "WARN", "ERROR"}


def timestamp() -> str:
    """Filesystem-safe timestamp, e.g. 20260615-102341."""
    return datetime.now().strftime("%Y%m%d-%H%M%S")


def log(level: str, message: str) -> None:
    """
    Append a line to migration.log. Levels: INFO, WARN, ERROR.
    Never pass secret values to this function (callers must redact first).
    """
    lvl = level.upper() if level.upper() in _VALID_LEVELS else "INFO"
    line = f"[{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}] [{lvl}] {message}"
    try:
        with open(LOG_FILE, "a", encoding="utf-8") as fh:
            fh.write(line + "\n")
    except OSError:
        pass  # logging must never crash the run


def safe_write(path: Path, content: str) -> bool:
    """
    Write text to *path*, backing up any existing file to {name}.backup.{timestamp} first.
    Verifies the write by reading back. Returns True on success. Never raises.
    """
    path = Path(path)
    try:
        path.parent.mkdir(parents=True, exist_ok=True)
        if path.exists():
            backup = path.parent / f"{path.name}.backup.{timestamp()}"
            shutil.copy2(path, backup)
            log("INFO", f"Backed up existing file to {backup}")
        path.write_text(content, encoding="utf-8")
        if path.exists():
            log("INFO", f"Written: {path}")
            return True
        log("ERROR", f"Write verification failed: {path}")
        return False
    except OSError as e:
        log("ERROR", f"Could not write {path}: {e}")
        return False


