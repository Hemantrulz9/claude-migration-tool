# --------------------------------------------------------------------------- helpers
def first_existing(candidates: list[Path]) -> Path | None:
    """Return the first path in *candidates* that exists, else None."""
    for c in candidates:
        try:
            if c and Path(c).exists():
                return Path(c)
        except OSError:
            continue
    return None


def glob_dirs(parent: Path, pattern: str) -> list[Path]:
    """Return immediate child directories of *parent* matching *pattern* (safe if absent)."""
    try:
        if parent and parent.exists():
            return sorted(p for p in parent.glob(pattern) if p.is_dir())
    except OSError:
        pass
    return []


def glob_files(parent: Path, pattern: str) -> list[Path]:
    """Return files under *parent* matching *pattern* recursively (safe if absent)."""
    try:
        if parent and parent.exists():
            return sorted(p for p in parent.rglob(pattern) if p.is_file())
    except OSError:
        pass
    return []


